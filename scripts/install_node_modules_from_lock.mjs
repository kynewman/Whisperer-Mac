#!/usr/bin/env node
// Minimal package-lock installer used when npm is not available on this Mac.
// It is intentionally narrow: it installs tarballs from lockfileVersion 3 into
// node_modules and skips packages that do not match the current os/cpu.

import { spawnSync } from "node:child_process";
import fs from "node:fs";
import https from "node:https";
import os from "node:os";
import path from "node:path";

const appDir = path.resolve(process.argv[2] || "whisperer-app");
const lockPath = path.join(appDir, "package-lock.json");
const nodeModules = path.join(appDir, "node_modules");
const tmpRoot = fs.mkdtempSync(path.join(os.tmpdir(), "whisperer-npm-"));
const currentOS = process.platform;
const currentCPU = process.arch;

function allowed(list, current) {
  if (!Array.isArray(list) || list.length === 0) return true;
  if (list.includes(`!${current}`)) return false;
  const positives = list.filter((item) => !String(item).startsWith("!"));
  return positives.length === 0 || positives.includes(current);
}

function wanted(meta) {
  return allowed(meta.os, currentOS) && allowed(meta.cpu, currentCPU);
}

function fetchBuffer(url, redirects = 0) {
  return new Promise((resolve, reject) => {
    https
      .get(url, (res) => {
        if ([301, 302, 303, 307, 308].includes(res.statusCode) && res.headers.location) {
          if (redirects > 5) {
            reject(new Error(`Too many redirects for ${url}`));
          } else {
            resolve(fetchBuffer(new URL(res.headers.location, url).toString(), redirects + 1));
          }
          res.resume();
          return;
        }
        if (res.statusCode !== 200) {
          reject(new Error(`HTTP ${res.statusCode} for ${url}`));
          res.resume();
          return;
        }
        const chunks = [];
        res.on("data", (chunk) => chunks.push(chunk));
        res.on("end", () => resolve(Buffer.concat(chunks)));
      })
      .on("error", reject);
  });
}

async function installOne(pkgPath, meta) {
  if (!pkgPath.startsWith("node_modules/") || !meta.resolved || !wanted(meta)) return false;
  const dest = path.join(appDir, pkgPath);
  fs.rmSync(dest, { recursive: true, force: true });
  fs.mkdirSync(dest, { recursive: true });
  const tarball = path.join(tmpRoot, `${pkgPath.replaceAll("/", "__")}.tgz`);
  fs.writeFileSync(tarball, await fetchBuffer(meta.resolved));
  const result = spawnSync("tar", ["-xzf", tarball, "-C", dest, "--strip-components", "1"], {
    encoding: "utf8",
  });
  if (result.status !== 0) {
    throw new Error(`tar failed for ${pkgPath}: ${result.stderr || result.stdout}`);
  }
  return true;
}

async function main() {
  const lock = JSON.parse(fs.readFileSync(lockPath, "utf8"));
  const entries = Object.entries(lock.packages || {}).filter(
    ([pkgPath, meta]) => pkgPath && meta && meta.resolved,
  );
  fs.rmSync(nodeModules, { recursive: true, force: true });
  fs.mkdirSync(nodeModules, { recursive: true });

  let installed = 0;
  for (let i = 0; i < entries.length; i += 6) {
    const chunk = entries.slice(i, i + 6);
    const results = await Promise.all(chunk.map(([pkgPath, meta]) => installOne(pkgPath, meta)));
    installed += results.filter(Boolean).length;
    process.stdout.write(`\rInstalled ${installed}/${entries.length} packages`);
  }
  process.stdout.write("\n");
}

main()
  .catch((err) => {
    console.error(err?.stack || String(err));
    process.exitCode = 1;
  })
  .finally(() => {
    fs.rmSync(tmpRoot, { recursive: true, force: true });
  });
